package Exercicios;

public class Cliente {

    String nome;
    int idade;
    String sexo;
    String email;
    String telefone;
    String cpf;


    public Cliente() {

    }

    public Cliente(String nome, int idade, String sexo, String email, String telefone, String cpf) {
        this.nome = nome;
        this.idade = idade;
        this.sexo = sexo;
        this.email = email;
        this.telefone = telefone;
        this.cpf = cpf;
    }
}
