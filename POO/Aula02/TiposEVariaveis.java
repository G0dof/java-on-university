public class TiposEVariaveis {
    public static void main(String[] args) {
           byte exByte = 127;
           short exShort = 32767;
           boolean exBoolean = true;
           int exInt = 1231231323;
           double exDouble = 311232.51521;
           float $dinheiro = 546.79F;

        System.out.println(exByte);
        System.out.println(exShort);
        System.out.println(exBoolean);
        System.out.println(exInt);
        System.out.println(exDouble);
        System.out.println($dinheiro);
    }
}
